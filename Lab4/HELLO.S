;WRITE A PROGRAM TO TRANSFER A BLOCK OF 256 WORDS STORED AT 
;MEMORY LOC X TO MEM LOC Y USING LOAD MULTIPLE AND STORE 
;MULTIPLE INSTRUCTION. THE RATE OF TRANSFER IS 32 BYTES 

.DATA 
    X: .WORD 1,2,3,4,5,6,7,8,9,110,11,22,33,44,55,66,77,88,99,110,34,56,67,87
    Y: .WORD 


.TEXT 
    LDR R0,=X
    LDR R1,=Y
    MOV R10,#0

    
    LOOP:
        LDMIA R0!,{R2,R3,R4,R5,R6,R7,R8,R9}
        STMIA R1!,{R2,R3,R4,R5,R6,R7,R8,R9}
        ADD R10,R10,#1
        CMP R10,#3
        BNE LOOP
        SWI 0X011